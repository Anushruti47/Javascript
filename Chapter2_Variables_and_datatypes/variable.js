var age=23;
console.log(age);

//Historical and less preferred
var age=25;
console.log(age);
console.log(age-1);

//Block-scoped
let name="John Doe";
console.log(name);

//Block-scoped and constant (cannot be reassigned)
const PI=3.14;
console.log(PI);

//Properties of variables
//Reassignment is possible in var and let unlike const

var age=23;
console.log(age);

let count=10;
console.log(count);

//Update the age 
age=24;
console.log(age);

//Update the count variable by 1
count=count+1;
console.log(count);

